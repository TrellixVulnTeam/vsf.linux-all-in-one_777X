static int app_argc;
static char **app_argv;

static void * app_instance_main(wasm_module_inst_t module_inst)
{
    const char *exception;

    wasm_application_execute_main(module_inst, app_argc, app_argv);
    if ((exception = wasm_runtime_get_exception(module_inst)))
        printf("%s\n", exception);
    return NULL;
}

int wamr_test_main(int argc, char *argv[])
{
    uint8_t *wasm_file_buf = NULL;
    uint32_t wasm_file_size;
    wasm_module_t wasm_module = NULL;
    wasm_module_inst_t wasm_module_inst = NULL;
    RuntimeInitArgs init_args;
    char error_buf[128];
#if WASM_ENABLE_LOG != 0
    int log_verbose_level = 2;
#endif

    memset(&init_args, 0, sizeof(RuntimeInitArgs));

    static char global_heap_buf[256 * 1024] = { 0 };
    init_args.mem_alloc_type = Alloc_With_Pool;
    init_args.mem_alloc_option.pool.heap_buf = global_heap_buf;
    init_args.mem_alloc_option.pool.heap_size = sizeof(global_heap_buf);

    /* initialize runtime environment */
    if (!wasm_runtime_full_init(&init_args)) {
        printf("Init runtime environment failed.\n");
        return 0;
    }

#if WASM_ENABLE_LOG != 0
    bh_log_set_verbose_level(log_verbose_level);
#endif

    /* load WASM byte buffer from byte buffer of include file */
//    wasm_file_buf = (uint8_t *)wasm_test_file;
//    wasm_file_size = sizeof(wasm_test_file);
    wasm_file_size = sizeof(wasm_test_file);
    wasm_file_buf = malloc(wasm_file_size);
    if (!wasm_file_buf) {
        printf("fail to allocate file buffer\n");
        goto fail1;
    }
    memcpy(wasm_file_buf, wasm_test_file, wasm_file_size);

    /* load WASM module */
    if (!(wasm_module = wasm_runtime_load(wasm_file_buf, wasm_file_size,
                                          error_buf, sizeof(error_buf)))) {
        printf("%s\n", error_buf);
        goto fail1;
    }

    /* instantiate the module */
    if (!(wasm_module_inst = wasm_runtime_instantiate(
              wasm_module, 8 * 1024, 8 * 1024, error_buf, sizeof(error_buf)))) {
        printf("%s\n", error_buf);
        goto fail2;
    }

    app_instance_main(wasm_module_inst);

    /* destroy the module instance */
    wasm_runtime_deinstantiate(wasm_module_inst);

fail2:
    /* unload the module */
    wasm_runtime_unload(wasm_module);

fail1:
    /* destroy runtime environment */
    wasm_runtime_destroy();
    if (wasm_file_buf != NULL) {
        free(wasm_file_buf);
    }
    return 0;
}
